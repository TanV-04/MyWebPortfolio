<div class="py-6">
  <h1 class="text-center text-3xl font-bold">How I Started My First Blog</h1>
</div>

<div class="prose px-4 mx-auto leading-loose">
  <p>
    Many of you bloggers out there have started at this step (obviously) and
    thought at first, whether blogging <span class="reallyWord">really</span> was
    your cup of tea or not. I'm standing there, at that step, really pondering and
    mulling over the thought. I searched countless websites, to find topics so that
    I would have something I could talk about in my first blog, and make a good first
    impression, otherwise, I'd just be rambling, and came up with no solutions, because
    out of all the amazing topics available, I had no idea what to talk about them.
  </p>
  <p>
    So, I thought, let's start small, with introductions. My name is Tanvi
    Sontakke, I'm a 17-year-old ardent reader, and a lazy procrastinator (not
    all procrastinators are lazy, though) when it comes to studies. I can say
    I'm responsible enough about my school assignments, tests, regular chores,
    and whatnot so that my parents, especially my mother, won't have to worry
    about me leaving my bed messy every time (although, I do get yelled at for
    forgetting).
  </p>
  <p>
    I can say that I'm an introvert, I guess? Sometimes I don't feel like one,
    so I'm certainly not a good example of an introverted person. I started my
    "becoming a bookworm" journey by reading <span class="percy"
      >Percy Jackson</span
    > when I was in middle school, which is the right time to start reading PJ, and
    slowly increased the thickness of the books as I entered high school. Now being
    a high school senior, I shouldn't have time for reading and lazing about, but
    this is a hobby I'll always find time for (I can hear all my fellow bookworms
    cheering in agreement, I hope!).
  </p>
  <p>
    If you're wondering what genre of books I prefer, well, I like fantasy,
    fiction, mystery, crime (not too much), and romedy, and if I'm in the mood,
    I'll manage to read a biography. I claim nobody was my inspiration for
    starting a blog, but I read so many blogs and editorials in the Times of
    India, that I felt a tiny spark in me (at 10:30 in the night!), and thought,
    hey, why not give this a shot, something new to do in this monotonous life
    I'm living.
  </p>
  <p>
    And so here I am, dear readers who took the pain and precious time to go
    through my first-ever blog, and for that, I thank you. I hope that my first
    official blog will be much more interesting than this one, till then, à
    bientôt, mes amis! (That's French for "See you soon, my friends!").
  </p>
</div>

