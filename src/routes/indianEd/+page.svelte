<div class="py-6">
  <h1 class="text-center text-3xl font-bold">To Be Or Not To Be</h1>
</div>

<div class="prose px-4 mx-auto leading-loose">
  <p>
    To be: a doctor, an engineer, a researcher or not to be: a journalist, a
    dancer, a singer, an artist, a photographer….
  </p>
  <p>
    The dubiety of choosing a stream, a degree, and a job has plagued students,
    college graduates and adults alike. They sit at a precipice, wondering
    whether they should make the choice for their parents and family, and plunge
    into the void of unhappiness and dissatisfaction that may affect their lives
    negatively, or make the decision themselves and choose to turn back to the
    safety of the cliff.
  </p>

  <p>
    In India, given the mentality of the society, students can rarely choose the
    latter (i.e. make their own choice). The entire Indian education system,
    parents, and teachers are burdening children with exorbitantly high
    expectations, and making them believe they aren’t supposed to fail in any of
    their endeavours, even when the child is not interested and not doing well
    in it. This leads to the belief that smart kids will automatically go for
    science, average for commerce, and below average, humanities. Parents
    believe that every child’s intellectual capacity is the same as the other,
    and that high intelligence is always associated with studying and scoring
    well.
  </p>

  <p>
    This leaves no room for creative, logical, linguistic, intra and
    inter-personal and artistic development in the child. He becomes a mindless
    robot, designed to please and follow orders from everyone, especially
    parents. A college graduate faces a similar but different dilemma, to take
    up a job of his choice, or the one his parents chose for him. He visits the
    precipice, and ponders for a long time, and given the pressure of parents
    and society, he decides to take the plunge.
  </p>

  <p>
    Although, there are parents who understand that there is a choice in
    education, and their children are capable of making decisions for
    themselves, but the majority tends to force or manipulate their kids into
    letting go of their dreams and ambitions and passions, and take the path
    that’ll make them richer. This obviously isn’t right. It’s time for them to
    understand, that following our heart and passion, although won’t make us
    richer, but it’ll make us happy.
  </p>

  <p>
    Some of us are not good at memorizing chemical equations, or sitting at
    boring desk jobs, we’d prefer to play football or paint out our emotions and
    feelings on a canvas. Times are changing, and students and adults can choose
    and do whatever they want, they have their roles in the world, and different
    forms of happiness, and being exceptional and making different choices from
    everyone else isn’t bad, it just makes them unique.
  </p>

  <p>So, to be or not to be, <strong>that</strong> is the question.</p>
</div>

