    MULTI- NO SORRY, IT’S METAVERSE
  </h1>
</div>

<div class="prose px-4 mx-auto leading-loose">
  <p>
    Following the rebranding of Facebook to Meta raised many questions as to
    what the terms “Meta” or “metaverse” meant. Although, the idea of it isn’t
    new, the textbook definition of metaverse states that “it is the
    hypothesized next iteration of the Internet, supporting decentralized,
    persistent online 3-D virtual environments.”
  </p>

  <p>
    But it’s so much more than the “<span class="iteration">next iteration of the Internet</span>”. It is a
    huge leap in artificial intelligence, virtual and augmented technology for
    humans to interact in 3-D avatars, holograms, and all those cool tech things
    we see in sci-fi movies. We’ll finally get to experience the Avengers’
    technology!
  </p>

  <p>
    The metaverse refers to a variety of virtual experiences, environments, and
    assets, which gained popularity during the pandemic last year. What I mean
    by that is, all the online classes, conferences, etc. were just a small part
    of the metaverse we experienced. In actuality, within the metaverse, the
    user’s avatar or hologram is created that allows him/her to interact with
    colleagues, students, classmates almost physically. For example, your avatar
    could also walk over and sit next to someone else’s avatar to start a
    conversation, or turn your gaze over to someone you’re addressing.
  </p>

  <p>
    Gamers playing augmented reality (AR) games like <span class="vt323-regular"
      >Minecraft</span
    >, <span class="luckiest-guy-regular">Fortnite</span>, and
    <span class="special-elite-regular">Roblox</span> are already treading into the
    zone of metaversitality, where they can create 3D worlds and characters. Virtual
    and augmented realities are metaverse adjacent. Together, these technologies
    are hinting at what the Internet will become in the near future.
  </p>

  <p>
    Founders, businessmen, futurists, investors have all tried to stake their
    claims on the idea of metaverse, and put forward its worth in social media,
    entertainment, experimentation, and profit. If this sounds too exciting or
    heady, just know the metaverse is a very advanced usage of the Internet, and
    it may still be in the future, but if it ever materializes, it is closer
    than it’s ever been.
  </p>
</div>

